package at.technikum.exercises.chapter1;

public class NeighborOfTheBeast {
    public static void main(String[] args) {
        System.out.printf("the neighbor of the beast: %d!\n", 667);
        System.out.printf("the neighbor of the beast: %X!\n", 667);
        System.out.printf("the neighbor of the beast: %10d!\n", 667);
        System.out.printf("the neighbor of the beast: %-10d!\n", 667);
        System.out.printf("the neighbor of the beast: %08d!\n", 667);
        System.out.printf("the neighbor of the beast: %+-12d!\n", 667);
    }
}
